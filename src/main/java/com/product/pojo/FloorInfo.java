package com.product.pojo;


public class FloorInfo {

  private int id;
  private String bh;
  private String name;
  private String louzt;
  private String xqmc;
  private String xqbh;
  private String rksj;


  public int getId() {
    return id;
  }

  public void setId(int id) {
    this.id = id;
  }


  public String getBh() {
    return bh;
  }

  public void setBh(String bh) {
    this.bh = bh;
  }


  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }


  public String getLouzt() {
    return louzt;
  }

  public void setLouzt(String louzt) {
    this.louzt = louzt;
  }


  public String getXqmc() {
    return xqmc;
  }

  public void setXqmc(String xqmc) {
    this.xqmc = xqmc;
  }


  public String getXqbh() {
    return xqbh;
  }

  public void setXqbh(String xqbh) {
    this.xqbh = xqbh;
  }


  public String getRksj() {
    return rksj;
  }

  public void setRksj(String rksj) {
    this.rksj = rksj;
  }

}
